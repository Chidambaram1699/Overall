package week1day2;

public class Operators {

	public static void main(String[] args) {
		System.out.println("5+2");
		System.out.println("5-2");
		System.out.println("5*2");
		System.out.println("5/2");
		System.out.println("5%1");
		System.out.println(5>2);
		System.out.println(5<2);
		System.out.println(5<=2);
		System.out.println(5>=2);
		System.out.println(5==2);
		System.out.println(5>2 && 7>2 );
		System.out.println(5<2 || 7>2);
		int a=3;
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
	}

}
